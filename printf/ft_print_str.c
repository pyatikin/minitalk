#include "ft_printf.h"

int	ft_print_str(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		s = ft_strdup("(null)");
		free(s);
	}
	while (*s)
	{
		ft_putchar(*s);
		s++;
		i++;
	}
	return (i);
}
