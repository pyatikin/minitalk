#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*mass;

	mass = (unsigned char *)s;
	while ((int)n--)
	{
		*mass++ = (unsigned char)c;
	}
	return (s);
}
